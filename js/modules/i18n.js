let currentLang = 'es';

/**
 * Sets active language and updates all data-i18n elements in the DOM.
 * @param {'es'|'en'} lang
 */
export function setLanguage(lang) {
  if (!translations[lang]) return;
  currentLang = lang;
  document.documentElement.lang = lang;
  document.documentElement.setAttribute('data-lang', lang);
  updateDOM();
}

/**
 * Returns translation string for a given key in the current language.
 * @param {string} key
 * @returns {string}
 */
export function t(key) {
  return translations[currentLang][key] || translations['es'][key] || key;
}

/**
 * Returns current active language.
 * @returns {'es'|'en'}
 */
export function getLang() {
  return currentLang;
}

/**
 * Updates all [data-i18n] elements in the DOM.
 */
function updateDOM() {
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    const text = t(key);
    if (text) el.textContent = text;
  });
}

/**
 * Initialises i18n and binds language toggle buttons.
 */
export function initI18n() {
  // Detect saved preference
  const saved = localStorage.getItem('alq-lang');
  if (saved && (saved === 'es' || saved === 'en')) {
    currentLang = saved;
  }
  updateDOM();

  // Bind toggle buttons
  document.querySelectorAll('[data-lang]').forEach(btn => {
    btn.addEventListener('click', () => {
      const lang = btn.getAttribute('data-lang');
      localStorage.setItem('alq-lang', lang);
      setLanguage(lang);
      // Notify calculator to refresh labels
      document.dispatchEvent(new CustomEvent('langchange', { detail: { lang } }));
    });
  });

  // Bind header lang toggle
  const headerToggle = document.getElementById('lang-toggle');
  if (headerToggle) {
    headerToggle.addEventListener('click', () => {
      const next = currentLang === 'es' ? 'en' : 'es';
      localStorage.setItem('alq-lang', next);
      setLanguage(next);
      document.dispatchEvent(new CustomEvent('langchange', { detail: { lang: next } }));
    });
  }
}
